for (let i of product_fruit.data) {
    // Tạo cột để hiển thị mỗi card (responsive 3 sản phẩm / hàng)
    let col = document.createElement("div");
    col.classList.add("col-md-4");

    // Tạo thẻ card
    let card = document.createElement("div");
    card.classList.add("card");
    card.style.width = "100%";

    // Hình ảnh
    let image = document.createElement("img");
    image.setAttribute("src", i.hinh); // Sử dụng đúng tên thuộc tính hình ảnh
    image.classList.add("card-img-top");
    image.setAttribute("alt", i.tensp);
    card.appendChild(image);

    // Thân card
    let cardBody = document.createElement("div");
    cardBody.classList.add("card-body");

    // Tên sản phẩm
    let title = document.createElement("h5");
    title.classList.add("card-title");
    title.innerText = i.tensp;

    // Giá sản phẩm
    let price = document.createElement("p");
    price.classList.add("card-text");
    price.innerHTML = `
        <del>${i.giachuagiam}</del><br>
        <strong class="text-danger">${i.giadagiam}</strong>
    `;

    // Nút mua (tùy chỉnh nếu cần)
    let button = document.createElement("a");
    button.classList.add("btn", "btn-primary");
    button.href = "#";
    button.innerText = "Xem chi tiết";

    // Gắn vào cardBody
    cardBody.appendChild(title);
    cardBody.appendChild(price);
    cardBody.appendChild(button);

    // Gắn cardBody vào card
    card.appendChild(cardBody);

    // Gắn card vào col
    col.appendChild(card);

    // Thêm vào DOM chính
    document.getElementById("product-content").appendChild(col);
}